    get_task = asyncio.ensure_future(queue.get())
    asyncio.ensure_future(put_item())
    item = await get_task
    assert item == 2

    # Test get with timeout
    with pytest.raises(asyncio.TimeoutError):
        await queue.get(timeout=0.1)


